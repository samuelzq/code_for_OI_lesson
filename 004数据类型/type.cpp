#include <bits/stdc++.h>

using namespace std;

int main(void)
{
	char ch2 = 'A';
	char str[20] = "Hello World";
	float flt = 10.234;
	int no = 150;
	double dbl = 20.123456;

	cout << "字符为 " << ch2 << endl;
	cout << "字符串为 " << str << endl;
	cout << "浮点数为 " << flt << endl;
	cout << "整数为 " << no << endl;
	cout << "双精度值为 " << dbl << endl;   // 保留六位有效数字
 	return 0;
}

